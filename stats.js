/*0 znaci da su statsi zatvoreni, 1 da su otvoreni*/ 
let toggleStatus = 0;

function toggleStats1(){
    if(toggleStatus == 0){
        document.querySelector(".overlayp1").style.display = "none";
        document.querySelector(".infop1").style.display = "unset";
        toggleStatus = 1;
        console.log("kokos");
    }

}
function closeStats1(){
    if(toggleStatus == 1){
        document.querySelector(".overlayp1").style.display = "flex";
        document.querySelector(".infop1").style.display = "none";
        toggleStatus = 0;
        console.log("bik");
    }
}

